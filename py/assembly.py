def _compute_sha256(file_path: str) -> str:
    h = hashlib.sha256()
    with open(file_path, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def _make_slug(filename: str, source_idx: int) -> str:
    """Create a URL-safe, source-specific anchor prefix from a filename and 1-based index."""
    stem = os.path.splitext(filename)[0]
    slug = re.sub(r'[^a-z0-9]+', '-', stem.lower()).strip('-')[:24].rstrip('-')
    return f"src{source_idx:02d}-{slug}"

def _assemble_gold_master(
    filename: str, meta: dict, sha256_hash: str, mask_mode: str,
    process_date: str, section3_md: str,
    file_slug: str = "", skip_all_masking: bool = False,
    condense_report: dict | None = None,
    include_preamble: bool = True
) -> str:
    content_type = meta.get("content_type", "Document")
    form = meta.get("form", "")
    form_name = meta.get("form_name", "")
    tax_year = meta.get("tax_year", "")
    tax_period = meta.get("tax_period", "")
    transcript_date = meta.get("transcript_date", "")
    page_count = meta.get("page_count", "?")
    titles = meta.get("potential_titles", [])
    doc_title = titles[0] if titles else filename
    author = meta.get("author", "")
    case_numbers = meta.get("case_numbers", [])
    sheets = meta.get("sheets", [])

    anchor_prefix = re.escape(file_slug) if file_slug else "page"
    extracted_pages = len(re.findall(rf'<a id="{anchor_prefix}-page-\d+"', section3_md))

    # RAG self-identification: suffix every page/slide heading with the source
    # filename so a retrieved CHUNK of a knowledge library names its document
    # ("### Page 12" alone is ambiguous across a multi-document library). Keyed
    # on the converter-generated anchor (optionally followed by Bates/blockquote
    # annotation lines) so verbatim source text that happens to start with "###"
    # is never touched. Anchors themselves are unchanged.
    section3_md = re.sub(
        rf'(<a id="{anchor_prefix}-page-\d+[^"]*"></a>(?:\s*\n>[^\n]*)*\s*\n+)(###[^\n]+)',
        lambda m: m.group(1) + m.group(2) + f" — {filename}",
        section3_md)

    image_only = section3_md.count("image-only or scanned page")
    unextractable = section3_md.count("No extractable text after filtering")
    anchors_unique = "Yes — source-specific prefix" if file_slug else "No — generic (single-file mode)"
    anchor_example = f"#{file_slug}-page-001" if file_slug else "#page-001"

    meta_rows = [
        f"| Source File | `{filename}` |",
        f"| Document Title | {doc_title} |",
        f"| Content Type | {content_type} |",
    ]
    if form:
        form_display = form + (f" — {form_name}" if form_name else "")
        meta_rows.append(f"| Form | {form_display} |")
    # Only show tax year/period for actual tax documents — prevents birth years
    # in medical records from appearing as false "Tax Year" metadata
    is_tax_doc = content_type == "Tax Form"
    if is_tax_doc and tax_period:
        meta_rows.append(f"| Tax Period | {tax_period} |")
    elif is_tax_doc and tax_year:
        meta_rows.append(f"| Tax Year | {tax_year} |")
    if transcript_date:
        meta_rows.append(f"| Transcript Date | {transcript_date} |")
    if author:
        meta_rows.append(f"| Author | {author} |")
    if case_numbers:
        meta_rows.append(f"| Case Numbers | {', '.join(case_numbers)} |")
    if sheets:
        label = "Sheets" if len(sheets) > 1 else "Sheet"
        meta_rows.append(f"| {label} | {', '.join(str(s) for s in sheets)} |")
    meta_rows += [
        f"| Pages / Sheets / Rows | {page_count} |",
        f"| [SHA-256](https://en.wikipedia.org/wiki/SHA-2) | `{sha256_hash}` |",
        f"| Mask Mode | {_mask_label(mask_mode, skip_all_masking)} |",
        f"| Processed | {process_date} |",
        f"| Generated By | AI Ready {VERSION} |",
    ]

    scanned_ratio = meta.get("scanned_ratio", 0)
    scanned_warning = ""
    if scanned_ratio >= 0.5:
        scanned_warning = f" ⚠ {int(scanned_ratio*100)}% of pages are scanned images — text extraction incomplete"

    validation_rows = [
        f"| Source pages reported | {page_count} |",
        f"| Anchored sections extracted | {extracted_pages} |",
        f"| Image-only / unextractable pages | {image_only + unextractable}{scanned_warning} |",
        f"| Page anchors unique (combined file) | {anchors_unique} |",
        f"| SHA-256 recorded | Yes |",
    ]
    deduped = meta.get("deduped_pages", 0)
    if deduped:
        validation_rows.append(
            f"| Duplicate pages collapsed | {deduped} byte-identical copies replaced "
            f"with a pointer to the first (anchors preserved; no data lost) |")

    # Source-data integrity — confirm numeric values survived extraction unchanged.
    integ = meta.get("integrity") or {}
    if integ.get("total"):
        if integ.get("ok"):
            integ_val = f"✓ PASS — all {integ['total']} source values confirmed present in extraction"
        else:
            ex = ", ".join(str(x) for x in integ.get("missing", [])[:8])
            integ_val = (f"⚠ REVIEW — {integ['missing_count']} of {integ['total']} source values "
                         f"({integ.get('pct', 0)}% preserved) could not be confirmed in the extraction"
                         + (f" (e.g. {ex})" if ex else "")
                         + ". Verify these against the source before relying on them.")
        validation_rows.append(f"| Source-data integrity (values & dates) | {integ_val} |")

    # OCR confidence — the integrity signal for SCANNED pages, where numeric-token
    # integrity cannot apply (no digital text layer to compare). Surfaced so the
    # scanned content carries a verifiable quality score, not silent uncertainty.
    ocr_conf = meta.get("ocr_confidence")
    if ocr_conf:
        low = ocr_conf.get("low_pages", 0)
        ocr_val = (f"{'✓' if low == 0 else '⚠'} {ocr_conf['pages']} scanned page(s) read by OCR — "
                   f"mean confidence {ocr_conf['mean']}%, lowest {ocr_conf['min']}%"
                   + (f"; **{low} page(s) flagged for review** (low mean confidence, or a "
                      f"load-bearing word misread on an otherwise-clean page) — verify those "
                      f"against the original" if low else ". Spot-check values against the original."))
        validation_rows.append(f"| OCR confidence (scanned-page integrity) | {ocr_val} |")

    # Per-page reference labels — Bates stamps or court-record "C-pages", tied to
    # THIS file. Surfaced so an AI knows the scheme and can navigate to a label
    # (each page's label is annotated after its anchor in Section 3 and indexed).
    bates = meta.get("bates")
    if bates and bates.get("start"):
        term = bates.get("label_term", "Bates No.")
        row_label = "Bates numbering" if bates.get("kind") == "Bates" else "Record-page labels"
        validation_rows.append(
            f"| {row_label} | **{bates['start']} – {bates['end']}** · "
            f"{bates['count']} pages · 1 per page. Ask for a {term} to navigate; "
            f"each page's label is shown after its page anchor in Section 3. |")

    # Tax Line Index — for Tax Form documents only
    # Provides AI-queryable structured table: Tax Year, Form, Line, Value, Source Page
    tax_line_index = meta.get("tax_line_index", [])
    tax_line_section = []
    if content_type == "Tax Form" and tax_line_index and not meta.get("tax_index_reliable", True):
        # Safety net: the extractor produced figures, but they failed form
        # arithmetic — the layout isn't one AI Ready can parse reliably. Withhold
        # the figures rather than present confident wrong tax numbers.
        form_display = meta.get("form", "Tax Form")
        tax_year_display = meta.get("tax_year") or meta.get("tax_period") or "?"
        tax_line_section = [
            "",
            "### Tax Line Index",
            "",
            f"> **⚠ TAX LINE INDEX WITHHELD — UNRELIABLE EXTRACTION.** "
            f"AI Ready could not reliably read structured line values from this "
            f"document's layout (Form: **{form_display}** · Tax Year: **{tax_year_display}**). "
            f"{meta.get('tax_index_unreliable_reason', '')} "
            f"**No structured figures are listed here, on purpose** — presenting them "
            f"would risk confident but wrong tax numbers. Use the verbatim text in "
            f"Section 3 and the original PDF as the authoritative record.",
            "",
        ]
    elif content_type == "Tax Form" and tax_line_index:
        form_display = meta.get("form", "Tax Form")
        tax_year_display = meta.get("tax_year") or meta.get("tax_period") or "?"
        # Completeness caveat — if any page could not be read (image-only /
        # unextractable), say so plainly so an omitted value is never mistaken for
        # a blank line.
        unread_pages = image_only + unextractable
        if unread_pages:
            unread_note = (
                f" **⚠ {unread_pages} page(s) in this document could not be read "
                f"(image-only or unextractable) — any tax line on those pages is "
                f"MISSING from this index. See Section 3 and the original PDF.**"
            )
        else:
            unread_note = ""
        tax_line_section = [
            "",
            "### Tax Line Index",
            "",
            f"> **Source-extracted values only. No derivation, estimation, or summarization.** "
            f"Every value in this table is read verbatim from the source document `{filename}`. "
            f"Tax Year: **{tax_year_display}** · Form: **{form_display}**. "
            f"Source page links point to the exact page in Section 3 where the value appears.",
            "",
            f"> **⚠ Completeness is NOT guaranteed.** This index lists only line values "
            f"AI Ready could read with high confidence. A line that is blank, $0, or whose "
            f"value could not be confidently read from the PDF is **omitted** here — an "
            f"omission does NOT mean the line is empty. Always verify both the figures AND "
            f"completeness against Section 3 and the original document before use.{unread_note}",
            "",
            "| Tax Year | Form | Line | Description | Column | Value | Source Page |",
            "|---|---|---|---|---|---|---|",
        ]
        for entry in tax_line_index[:600]:   # cap generously — every form line + grid cell
            desc = entry.get("description") or "—"
            col = entry.get("column") or "—"   # grid forms (Sch E etc.) set this; 1040 leaves it blank
            tax_line_section.append(
                f"| {entry.get('tax_year','?')} | {entry.get('form','?')} | "
                f"{entry.get('line','?')} | {desc} | {col} | "
                f"{entry.get('value','?')} | {entry.get('page','—')} |"
            )
        tax_line_section.append("")

    # Medical Value Index — for Medical Document types only. The analog of the
    # Tax Line Index: every discrete clinical test/result, traceable to a source
    # page, abnormal results flagged. Withholds (rather than guess) on narrative,
    # graphical, or reference-text layouts it cannot parse confidently.
    med_index = meta.get("medical_value_index", [])
    med_value_section = []

    def _cell(s):
        return str(s or "").replace("|", "\\|").strip() or "—"

    if content_type == "Medical Document" and not meta.get("medical_index_reliable", True):
        med_value_section = [
            "",
            "### Medical Value Index",
            "",
            f"> **ℹ NO STRUCTURED VALUE INDEX FOR THIS DOCUMENT.** "
            f"{meta.get('medical_index_unreliable_reason', '')} "
            f"This is expected for narrative reports (radiology, pathology, clinical "
            f"notes), graphically-plotted panels, and reference material. The verbatim "
            f"text in Section 3 and the original PDF are the authoritative record.",
            "",
        ]
    elif content_type == "Medical Document" and med_index:
        abnormal = sum(1 for e in med_index if e.get("flag"))
        unread_pages = image_only + unextractable
        unread_note = (
            f" **⚠ {unread_pages} page(s) could not be read (image-only or "
            f"unextractable) — any value on those pages is MISSING from this index.**"
            if unread_pages else ""
        )
        med_value_section = [
            "",
            "### Medical Value Index",
            "",
            f"> **Source-extracted clinical values only. No derivation, estimation, or "
            f"interpretation.** Every test and result below is read verbatim from "
            f"`{filename}` and links to the source page in Section 3. "
            f"{abnormal} value(s) carry an abnormal flag (H/L/Critical) as marked in the source.",
            "",
            f"> **⚠ Completeness is NOT guaranteed.** This index lists only discrete "
            f"values AI Ready could read with high confidence (test name paired with a "
            f"result and units or a reference range). A value rendered as a chart/graphic, "
            f"embedded in narrative prose, or whose result could not be confidently read "
            f"is **omitted** — an omission does NOT mean the test is absent. Always verify "
            f"figures AND completeness against Section 3 and the original document.{unread_note}",
            "",
            "| Panel | Test | Result | Units | Reference Range | Flag | Source Page |",
            "|---|---|---|---|---|---|---|",
        ]
        for e in med_index[:1500]:
            page_link = f"[p.{e['page']}](#{e['anchor']})" if e.get("anchor") else f"p.{e.get('page','—')}"
            med_value_section.append(
                f"| {_cell(e.get('panel'))} | {_cell(e.get('test'))} | "
                f"{_cell(e.get('result'))} | {_cell(e.get('units'))} | "
                f"{_cell(e.get('ref_range'))} | {_cell(e.get('flag'))} | {page_link} |"
            )
        med_value_section.append("")

    # Document Index — the UNIVERSAL audit fingerprint. Shown for every document
    # whose format is text (i.e. that does not already have a reliable tabular
    # row/column index — tax line index or medical value index). The decision is by
    # FORMAT, not document type: a tabular form gets its grid index; everything else
    # — legal, LLC, insurance, financial statements, generic PDFs, and any tax/
    # medical doc whose tabular index was withheld — gets this. So a misclassified
    # document is never left unaudited. Indexes the auditable elements (parties,
    # dates, amounts, defined terms, numbered references/clauses/statutes/citations,
    # section/exhibit headings, case numbers), each traceable to its source page.
    legal_index = meta.get("legal_index", [])
    _has_tabular = (
        (content_type == "Tax Form" and tax_line_index and meta.get("tax_index_reliable", True))
        or (content_type == "Medical Document" and med_index and meta.get("medical_index_reliable", True))
    )
    _show_doc_index = not _has_tabular
