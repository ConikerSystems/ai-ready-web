def _doc_order_key(name: str):
    """Sort key for ordering source documents NEWEST-FIRST. Extracts a date
    (`YY`/`YYYY` then `MM` then `DD`, separated by `.`/`-`/`_`/`/`) from anywhere in the
    filename or relative path; dated files come first, ordered newest→oldest, and
    undated files follow in name order. Heuristic — relies on the date-in-name
    convention; names without a recognizable date just fall back to alphabetical."""
    m = re.search(r'(?<!\d)((?:19|20)?\d{2})[.\-_/](\d{1,2})[.\-_/](\d{1,2})(?!\d)',
                  name or "")
    if m:
        y, mo, d = m.group(1), m.group(2), m.group(3)
        if len(y) == 2:
            y = "20" + y
        try:
            val = int(y) * 10000 + int(mo) * 100 + int(d)
            return (0, -val, name or "")     # dated first, newest (largest) first
        except ValueError:
            pass
    return (1, 0, name or "")                 # undated last, by name

