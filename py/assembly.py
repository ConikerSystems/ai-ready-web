def _masking_rule(mask_mode: str, skip_all_masking: bool = False) -> str:
    """Rule 4 of the AI preamble, stated ACCURATELY for THIS file's masking — so an
    unmasked file is never described as redacted (the old static rule always said
    'Respect masking', which was wrong on a no-mask run). Unmasked files say so plainly,
    so neither a human nor an AI mistakes a real value for a redaction; masked files keep
    the do-not-reconstruct instruction and name the mode."""
    if skip_all_masking or mask_mode == "none":
        return (
            ">\n"
            "> **4. This file is UNMASKED.** No redaction was applied — every name, date, number, "
            "and identifier in Section 3 is the **real, verbatim value** from the source. There are "
            "no `[… MASKED]` placeholders here; treat all values as authoritative.\n"
        )
    label = "full" if mask_mode == "full" else "soft"
    return (
        ">\n"
        f"> **4. This file is MASKED ({label} mask).** Some values were intentionally redacted and "
        "appear as `[DOB MASKED]`, `[SSN MASKED]`, `[NAME MASKED]`, or similar. Do not attempt to "
        "infer or reconstruct them.\n"
    )


def _mask_label(mask_mode: str, skip_all_masking: bool = False) -> str:
    """Short, human-clear masking status for metadata rows and headers, so a file in a
    mixed library (some masked, some not) is unambiguous at a glance."""
    if skip_all_masking or mask_mode == "none":
        return "none — UNMASKED (all values verbatim from source)"
    return f"{mask_mode} — MASKED"


def _front_matter(kind: str, fields: list) -> str:
    """Machine-readable YAML front matter for every output file (tooling can
    parse provenance without regex; AI assistants read it as plain metadata).
    Values are JSON-encoded, which is valid YAML and handles quotes/colons in
    filenames safely."""
    lines = ["---", f"kind: {kind}"]
    for key, value in fields:
        lines.append(f"{key}: {json.dumps(value, ensure_ascii=False)}")
    lines += ["---", ""]
    return "\n".join(lines) + "\n"

